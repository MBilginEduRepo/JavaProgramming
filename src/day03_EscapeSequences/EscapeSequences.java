package day03_EscapeSequences;

/*
Escape Sequences: Must be given with double quote
    \n: Starts a new line
    \t: paragraph space(tab)
    \\: single backslash
    \": double quotes
 */
public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Java \nPython \nC#");

        System.out.println("-----------------------");

        System.out.println("Hello Cydeo \nHow are you all today? \nIt's nice to see you all!\nWhat class do we have next class?");

        System.out.println("---------------------------------------------");

        System.out.println("\tJava is Cool Programming Language");

        System.out.println("---------------------------");

        System.out.println("/ \\"); // in order to one backward slash we need to give two backward slashes

        System.out.println("----------------------------");

        System.out.println("My favorite Tv show is \"Game of Throne\""); // in order to print double quotes we need to use \"


    }

}
