package day05_Concatenation;

public class StudentInfo {
    public static void main(String[] args) {

        String student_name = "Yavuz";
        int age = 32;
        char gender = 'M';
        String school_name = "Cydeo";
        String phone_number1 = "(382)-215-1704";
        String phone_number2 = "(212)-215-1704";
        boolean full_time = true;

        System.out.println("student_name = " + student_name);
        System.out.println("age = " + age);
        System.out.println("gender = " + gender);
        System.out.println("school_name = " + school_name);
        System.out.println("phone_number1 = " + phone_number1);
        System.out.println("phone_number2 = " + phone_number2);
        System.out.println("full_time = " + full_time);

    }
}
